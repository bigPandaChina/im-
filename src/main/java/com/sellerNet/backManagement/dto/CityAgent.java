package com.sellerNet.backManagement.dto;


public class CityAgent
{
	private Integer id;
	private Integer area_id;
	private Integer area_type;
	private Integer agent_integral;
	private Integer area_status;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getArea_id() {
		return area_id;
	}
	public void setArea_id(Integer area_id) {
		this.area_id = area_id;
	}
	public Integer getArea_type() {
		return area_type;
	}
	public void setArea_type(Integer area_type) {
		this.area_type = area_type;
	}
	public Integer getAgent_integral() {
		return agent_integral;
	}
	public void setAgent_integral(Integer agent_integral) {
		this.agent_integral = agent_integral;
	}
	public Integer getArea_status() {
		return area_status;
	}
	public void setArea_status(Integer area_status) {
		this.area_status = area_status;
	}
}